package ru.gb.hw1.regular;

/**
 * Класс с арифметическими функциями
 */
public class OtherClass {
    /**
     * @param a число "a"
     * @param b число "b"
     * @return возвращает сумму чисел "a" и "b"
     */
    public static int add(int a, int b) {
        return a + b;
    }

    /**
     * @param a число "a"
     * @param b число "b"
     * @return возвращает разность чисел "a" и "b"
     */
    public static int div(int a, int b) {
        return a - b;
    }

    /**
     * @param a число "a"
     * @param b число "b"
     * @return возвращает произведение чисел "a" и "b"
     */
    public static int mul(int a, int b) {
        return a * b;
    }

    /**
     * @param a число "a"
     * @param b число "b"
     * @return возвращает деление числа "a" на число "b"
     */
    public static int sub(int a, int b) {
        return a / b;
    }
}
